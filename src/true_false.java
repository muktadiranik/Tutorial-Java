public class true_false {
    public static void main(String[] args) {
        boolean a = true;
        if (a) {
            System.out.println(a);
        }
        else {
            System.out.println(a);
        }


        boolean b = false;

        if (!b) {
            System.out.println(b);
        }
        else {
            System.out.println(b);
        }

    }
}
